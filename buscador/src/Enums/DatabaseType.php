<?php

namespace App\Enums;

/**
 * Class DatabaseType
 *
 * Enumeration class representing different types of databases.
 *
 * @package App\Enums
 */
class DatabaseType
{
    /**
     * Represents the MySQL database type.
     */
    const MYSQL = 'MySQL';

    /**
     * Represents the PostgreSQL database type.
     */
    const POSTGRESSQL = 'PostgresSQL';

    /**
     * Represents the SQL Server database type.
     */
    const SQLS = 'SQLServer';
}
